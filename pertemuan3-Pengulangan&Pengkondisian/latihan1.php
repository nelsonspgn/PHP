<?php
//pengulangan
//for
//while
//do.. while
//foreach : pengulangan khusus array

//Menggunakan For
for ($i = 0 ; $i < 5 ; $i++){

    echo "Hello World! for <br>";
}
echo "<br>";
//Menggunakan While
$i = 0;
while ( $i < 5 ){

    echo "Hello World! while <br>";
    $i++;
}
echo "<br>";
//Menggunakan do..While
$i = 0;
do {
    echo "Hello World! do..while <br>";
    $i++;
} while ( $i < 5);
?>